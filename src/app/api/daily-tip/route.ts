/**
 * 文字列から決定論的なハッシュ値 (32bit)
 * 同じ入力なら同じ出力 → 「同じ日・同じユーザー」は必ず同じtipに
 */
function hash32(s: string): number {
  let h = 2166136261;
  for (let i = 0; i < s.length; i++) {
    h ^= s.charCodeAt(i);
    h = Math.imul(h, 16777619);
  }
  return h >>> 0;
}

function pickTip(month: number, dateKey: string, deviceId: string): Tip {
  const tips = SEASONAL_TIPS[month] || SEASONAL_TIPS[1];
  const key = `${dateKey}|${deviceId || "anon"}`;
  const idx = hash32(key) % tips.length;
  return tips[idx];
}

export async function GET(req: NextRequest) {
  const url = new URL(req.url);
  const deviceId = url.searchParams.get("deviceId") || "anon";
  const now = new Date();
  const month = now.getMonth() + 1;
  const dateKey = now.toISOString().slice(0, 10); // YYYY-MM-DD

  const tip = pickTip(month, dateKey, deviceId);
  const monthlyCount = (SEASONAL_TIPS[month] || []).length;

  return NextResponse.json({
    date: dateKey,
    month,
    tip,
    monthlyVariations: monthlyCount, // 月のバリエーション数
  });
}
